  // ==================== HELPER METHODS ====================

  /// Get icon for status
  static IconData getStatusIcon(String status) {
    final statusLower = status.toLowerCase();
    switch (statusLower) {
      case 'approved':
      case 'completed':
      case 'success':
        return success;
      case 'pending':
        return pending;
      case 'processing':
      case 'in_progress':
        return processing;
      case 'rejected':
      case 'failed':
      case 'error':
        return error;
      case 'cancelled':
      case 'inactive':
        return cancel;
      default:
        return info;
    }
  }

  /// Get icon for module
  static IconData getModuleIcon(String module) {
    final moduleLower = module.toLowerCase();
    switch (moduleLower) {
      case 'home':
      case 'dashboard':
        return home;
      case 'orders':
        return orders;
      case 'cash':
      case 'cashmanagement':
        return cash;
      case 'inventory':
        return inventory;
      case 'profile':
      case 'account':
        return profile;
      case 'reports':
        return report;
      case 'settings':
        return settings;
      case 'delivery':
        return delivery;
      case 'warehouse':
        return warehouse;
      case 'customer':
        return customer;
      default:
        return info;
    }
  }
